-- C.3) BLOQUEO EXPLÍCITO CON DBMS_LOCK.SLEEP
----------------------------------------------------

-- Sesión 1 (USERDBII): proceso largo que bloquea el producto P001
SET SERVEROUTPUT ON;

BEGIN 
    UPDATE PRODUCTO 
    SET    PRD_EXISTENCIA = PRD_EXISTENCIA - 30 
    WHERE  PRD_CODIGO = 'P001';

    DBMS_LOCK.SLEEP(40);  -- Se queda en espera 40 seg

    COMMIT;

    DBMS_OUTPUT.PUT_LINE('USERDBII: Usuario modificó producto P001');
END;
/
-- Mientras se ejecuta el SLEEP, ir a la Sesión 2 (BD2)

-- Sesión 2 (BD2): intenta actualizar el mismo producto
UPDATE USERDBII.PRODUCTO
SET    PRD_EXISTENCIA = PRD_EXISTENCIA + 80
WHERE  PRD_CODIGO = 'P001';
-- Esta sentencia se quedará bloqueada hasta que termine la transacción de USERDBII.


--------------------------------------------------------
-- C.4) MANEJO DE ERRORES EN TRANSACCIONES (EN BD2)
--------------------------------------------------------

-- Ahora probamos una transacción que intenta dejar en negativo la existencia
-- Sesión 2 (BD2)
SET SERVEROUTPUT ON;

BEGIN
    UPDATE USERDBII.PRODUCTO
    SET    PRD_EXISTENCIA = PRD_EXISTENCIA - 500
    WHERE  PRD_CODIGO = 'P001';

    UPDATE USERDBII.PRODUCTO
    SET    PRD_EXISTENCIA = PRD_EXISTENCIA - 20
    WHERE  PRD_CODIGO = 'P002';

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('BD2: Transacción completada correctamente');
EXCEPTION 
    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE(
          'ERROR: ' || SQLERRM || ' - Rollback automático ejecutado'
        );
END;
/

-- Verificamos desde USERDBII que NO se hicieron cambios si hubo error
-- (es decir, atomicidad de la transacción)
-- Sesión 1 (USERDBII):
SELECT PRD_CODIGO, PRD_NOMBRE, PRD_EXISTENCIA
FROM   PRODUCTO
WHERE  PRD_CODIGO IN ('P001', 'P002');


