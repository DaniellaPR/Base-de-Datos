-------------------------
-- 2) Tablas de bodegas y productos
-------------------------

-- TABLA BODEGA
CREATE TABLE BODEGA (
  BOD_CODIGO      VARCHAR2(4)    NOT NULL,
  BOD_DESCRIPCION VARCHAR2(60),
  CONSTRAINT PK_BODEGA PRIMARY KEY (BOD_CODIGO)
);
/

-- TABLA PRODUCTO
-- NOTA: se deja una estructura "completa" para que sirva
-- tanto al taller de bodega como al taller de pedidos.
CREATE TABLE PRODUCTO (
  PRD_CODIGO      VARCHAR2(12)   NOT NULL,
  PRD_NOMBRE      VARCHAR2(60),
  PRD_DESCRIPCION VARCHAR2(200),
  PRD_PRECIO      NUMBER(8,2),
  PRD_EXISTENCIA  NUMBER(10,2),
  CONSTRAINT PK_PRODUCTO PRIMARY KEY (PRD_CODIGO)
);
/

-- TABLA INTERMEDIA BODEGA_PRODUCTO
CREATE TABLE BODEGA_PRODUCTO (
  BOD_CODIGO     VARCHAR2(4)  NOT NULL,
  PRD_CODIGO     VARCHAR2(12) NOT NULL,
  PRB_EXISTENCIA NUMBER(10,2),
  CONSTRAINT PK_BODEGA_PRODUCTO PRIMARY KEY (BOD_CODIGO, PRD_CODIGO),
  CONSTRAINT FK_BP_BODEGA   FOREIGN KEY (BOD_CODIGO) REFERENCES BODEGA(BOD_CODIGO),
  CONSTRAINT FK_BP_PRODUCTO FOREIGN KEY (PRD_CODIGO) REFERENCES PRODUCTO(PRD_CODIGO)
);
/

-- Comentarios (opcional)
COMMENT ON TABLE BODEGA IS 'Entidad que almacena información de las bodegas';
COMMENT ON TABLE PRODUCTO IS 'Entidad que almacena los productos';
COMMENT ON TABLE BODEGA_PRODUCTO IS 'Relación N:N entre bodega y producto';
/

-------------------------
-- 3) Tablas de clientes y pedidos
-------------------------

-- CLIENTE
CREATE TABLE CLIENTE (
  CLI_CED_RUC   VARCHAR2(13)   NOT NULL,
  CLI_NOMBRE    VARCHAR2(100),
  CLI_DIRECCION VARCHAR2(200),
  CLI_TELEFONO  VARCHAR2(20),
  CLI_EMAIL     VARCHAR2(100),
  CONSTRAINT PK_CLIENTE PRIMARY KEY (CLI_CED_RUC)
);
/

-- PEDIDOCLIENTE
CREATE TABLE PEDIDOCLIENTE (
  PED_NUMERO  NUMBER(10)   NOT NULL,
  CLI_CED_RUC VARCHAR2(13) NOT NULL,
  PED_FECHA   DATE,
  PED_ESTADO  VARCHAR2(20),
  CONSTRAINT PK_PEDIDOCLIENTE PRIMARY KEY (PED_NUMERO),
  CONSTRAINT FK_PEDCLI_CLIENTE FOREIGN KEY (CLI_CED_RUC)
    REFERENCES CLIENTE(CLI_CED_RUC)
);
/

-- PRODUCTO_PEDCLI (tabla intermedia producto-pedido)
CREATE TABLE PRODUCTO_PEDCLI (
  PRD_CODIGO   VARCHAR2(12) NOT NULL,
  PED_NUMERO   NUMBER(10)   NOT NULL,
  PPC_CANTIDAD NUMBER(10,2),
  CONSTRAINT PK_PRODUCTO_PEDCLI PRIMARY KEY (PRD_CODIGO, PED_NUMERO),
  CONSTRAINT FK_PPC_PRODUCTO FOREIGN KEY (PRD_CODIGO)
    REFERENCES PRODUCTO(PRD_CODIGO),
  CONSTRAINT FK_PPC_PEDIDO FOREIGN KEY (PED_NUMERO)
    REFERENCES PEDIDOCLIENTE(PED_NUMERO)
);
/

-- PAGO
CREATE TABLE PAGO (
  PAG_CODIGO VARCHAR2(10) NOT NULL,
  PED_NUMERO NUMBER(10)   NOT NULL,
  PAG_MONTO  NUMBER(10,2),
  PAG_FECHA  DATE,
  CONSTRAINT PK_PAGO PRIMARY KEY (PAG_CODIGO),
  CONSTRAINT FK_PAGO_PEDIDO FOREIGN KEY (PED_NUMERO)
    REFERENCES PEDIDOCLIENTE(PED_NUMERO)
);
/

